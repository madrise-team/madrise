	local carToBuyKey = getElementData(theVehicle,"carToBuyKey")
	if carToBuyKey then
		setVehicleEngineState(theVehicle, false)
		triggerClientEvent(source,"buyTheCar",root,avtoSalonsCars[carToBuyKey],carToBuyKey)
	end
end)


