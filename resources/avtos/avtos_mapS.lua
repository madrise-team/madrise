function getMinDist(elm, vhod)
	local x, y, z = getElementPosition(elm)

	local rast1 = getDistanceBetweenPoints3D(x, y, z, vhod[2][1], vhod[2][2], vhod[2][3])
	local rast2 = getDistanceBetweenPoints3D(x, y, z, vhod[3][1], vhod[3][2], vhod[3][3])

	math.min(rast1, rast2)

	if rast1 < rast2 then
		return vhod[3][4]
	else
		return vhod[2][4]
	end
end

