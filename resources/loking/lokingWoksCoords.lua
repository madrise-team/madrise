function createSmesPoints(array)
	local newArray = {}

	if not array then
		outputChatBox("------ >>> ! createSmesPoints arg table is is Nil!!")
		return
	end

	local lastI = 1
	for i,valu in ipairs(array) do
		for i=1,valu.smes[4] do
			local smesVal = copy(valu)
			smesVal[2] = smesVal[2] + valu.smes[1]*(i-1)
			smesVal[3] = smesVal[3] + valu.smes[2]*(i-1)
			smesVal[4] = smesVal[4] + valu.smes[3]*(i-1)

			newArray[lastI] = smesVal
			lastI = lastI + 1

			createMarker (smesVal[2],smesVal[3],smesVal[4],"checkpoint",2, 0, 150, 150)
		end
	end
	return newArray
end




